<p>Is a system operated by the Students Care NGO. Student Complaint Support Management System is one of latest productivity enhancement tools used widely by many tertiary institutions.</p>
<p>The system is also a paramount tool wherever there is a need of booking of complaints via operators and analysis of complaints which are made or are pending. Lack of paper movements provides complaint management operations a speed which was never envisaged in manual mode at all. Software allows a booking operator to book and lodge complaints and automatically schedules and prompts operators to source complaint to concerned unit. </p>
   
